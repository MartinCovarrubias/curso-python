#operadores aritmeticos sin variables
print (4+5)  #suma
print (4-5)  #resta
print (40*5)  #multiplicacion
print (4/5)  #division
print (4%5)  #modulo (residuo de la division)
print (4**5) #exponente (potencia)
print (4//5) #cociente (division entera) solo la parte entera del resultado

#operadores aritmeticos con variables
print ("operadores aritmeticos con variables")
num1 = 4
num2 = 5
print (num1 +num2 )  #suma
print (num1 -num2 )  #resta
print (num1 *num2 )  #multiplicacion
print (num1 /num2 )  #division
print (num1 %num2 )  #modulo (residuo de la division)
print (num1 **num2 ) #exponente (potencia)
print (num1 //num2 ) #cociente (division entera) solo la parte entera del resultado


