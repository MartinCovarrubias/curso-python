# en python solo hay dos tipos de datos numericos enteros y flotantes
num1 = 40
num2 = 90.5

print(type(num1)) # para saber el tipo de dato
print(num1) # para imprimir el valor de la variable
print(float(num1)) #convertir un entero a flotante

# para convertir un flotante a entero
print(type(num2)) # para saber el tipo de dato
print(num2) # para imprimir el valor de la variable
print(int(num2)) #convertir un flotante a entero
print(type(int(num2)))
