# Condicionales if-else

edad = 12

#ejemplo if
# //! en python no se usa parentesis para las condiciones solo dos puntos al final de la condicion
# //! y la linea de codigo a ejecutar debe estar indentada
if edad >= 18:
    print("Eres mayor de edad")

#ejemplo if-else

if edad >= 18:
    print("Eres mayor de edad")
else:
    print("Eres menor de edad")