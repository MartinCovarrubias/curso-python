#suelen manejarse con strings

cadena = "Estoy mostrando los metodos boolea nos para las strings"

print(cadena.startswith("E") )# startswith se usa para saber si una cadena empieza con un caracter o una palabra
print(cadena.endswith("s") )# endswith se usa para saber si una cadena termina con un caracter o una palabra 
print(cadena.isalnum() )# isalnum se usa para saber si una cadena es alfanumerica
print(cadena.isalpha() )# isalpha se usa para saber si una cadena es alfabetica
print(cadena.isdigit() )# isdigit se usa para saber si una cadena es numerica
print(cadena.islower() )# islower se usa para saber si una cadena esta en minusculas
print(cadena.isupper() )# isupper se usa para saber si una cadena esta en mayusculas