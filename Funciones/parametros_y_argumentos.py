
#* un parametro es una variable que se va utilizar cuando se crea la funcion
#* un argumento es el valor que se le pasa a la funcion cuando se llama a la funcion

#? ejemplo de funcion con parametros

def suma(a, b): # a y b son parametros
    return a + b

#? ejemplo de funcion con argumentos

print(suma(2, 3)) # 2 y 3 son argumentos

#! Usando parametros y argumentos podemos reutilizar funciones