
#? Funciones integradas de Python

# funcion print()
print("Hola Mundo") # esta funcion imprime en pantalla el texto que le pasemos como argumento

# funcion type()
print(type(1)) # esta funcion nos devuelve el tipo de dato que le pasemos como argumento

# funcion len()
print(len("Hola Mundo")) # esta funcion nos devuelve la longitud de un string

# funcion input()
nombre = input("Â¿Cual es tu nombre? ") # esta funcion nos permite pedirle al usuario que ingrese un dato

# funcion int()
print(int("1")) # esta funcion nos permite convertir un string en un entero

# funcion str()
print(str(1)) # esta funcion nos permite convertir un entero en un string

# funcion float()
print(float(1)) # esta funcion nos permite convertir un entero en un flotante

# funcion list()
print(list("Hola Mundo")) # esta funcion nos permite convertir un string en una lista

# funcion tuple()
print(tuple("Hola Mundo")) # esta funcion nos permite convertir un string en una tupla

# funcion set()
print(set("Hola Mundo")) # esta funcion nos permite convertir un string en un conjunto

# funcion dict()
print(dict([("nombre", "Juan"), ("apellido", "Perez")])) # esta funcion nos permite convertir una lista de tuplas en un diccionario

# funcion range()
print(range(1, 10)) # esta funcion nos permite crear un rango de numeros

# funcion enumerate()
print(list(enumerate("Hola Mundo"))) # esta funcion nos permite enumerar los elementos de una lista

# funcion zip() 
print(list(zip([1, 2, 3], ["a", "b", "c"]))) # esta funcion nos permite unir dos listas en una lista de tuplas

# funcion max()
print(max([1, 2, 3])) # esta funcion nos permite obtener el valor maximo de una lista

# funcion min()
print(min([1, 2, 3])) # esta funcion nos permite obtener el valor minimo de una lista

# funcion sum()
print(sum([1, 2, 3])) # esta funcion nos permite sumar los valores de una lista

# funcion sorted()
print(sorted([3, 2, 1])) # esta funcion nos permite ordenar los valores de una lista

# funcion reversed()
print(list(reversed([1, 2, 3]))) # esta funcion nos permite invertir los valores de una lista




