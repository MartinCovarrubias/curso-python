
#? Funciones matemáticas

# para utilizar las siguientes funciones es necesario importar el módulo math

import math 

#! funciones de la librería math ejemplos

print(math.pi) # devuelve el valor de pi

print(math.e) # devuelve el valor de e

print(math.sin(0)) # devuelve el seno de 0

print(math.cos(0)) # devuelve el coseno de 0

print(math.tan(0)) # devuelve la tangente de 0

print(math.asin(0)) # devuelve el arcoseno de 0

print(math.acos(0)) # devuelve el arcocoseno de 0

print(math.atan(0)) # devuelve el arcotangente de 0

print(math.sinh(0)) # devuelve el seno hiperbólico de 0

print(math.cosh(0)) # devuelve el coseno hiperbólico de 0

print(math.tanh(0)) # devuelve la tangente hiperbólica de 0

print(math.asinh(0)) # devuelve el arcoseno hiperbólico de 0

print(math.acosh(0)) # devuelve el arcocoseno hiperbólico de 0

print(math.atanh(0)) # devuelve el arcotangente hiperbólica de 0

print(math.degrees(0)) # devuelve el valor de 0 en grados

print(math.radians(0)) # devuelve el valor de 0 en radianes

print(math.exp(0)) # devuelve el valor de e elevado a 0

print(math.log(0)) # devuelve el logaritmo natural de 0

print(math.log10(0)) # devuelve el logaritmo base 10 de 0

print(math.log2(0)) # devuelve el logaritmo base 2 de 0

print(math.sqrt(0)) # devuelve la raíz cuadrada de 0

print(math.ceil(0)) # devuelve el entero más pequeño mayor o igual a 0

print(math.floor(0)) # devuelve el entero más grande menor o igual a 0

print(math.fabs(0)) # devuelve el valor absoluto de 0

print(math.factorial(0)) # devuelve el factorial de 0

print(math.gcd(0, 0)) # devuelve el máximo común divisor de 0 y 0

print(math.isfinite(0)) # devuelve True si 0 es un número finito

print(math.isinf(0)) # devuelve True si 0 es infinito

print(math.isnan(0)) # devuelve True si 0 es un número no válido

print(math.trunc(0)) # devuelve la parte entera de 0

print(math.pow(0, 0)) # devuelve 0 elevado a 0

print(math.fmod(0, 0)) # devuelve el resto de la división de 0 entre 0

print(math.modf(0)) # devuelve la parte entera y la fraccionaria de 0

print(math.copysign(0, 0)) # devuelve 0 con el signo de 0

print(math.fsum([5, 3])) # devuelve la suma de los elementos de la lista

print(math.remainder(0, 0)) # devuelve el resto de la división de 0 entre 0

print(math.gcd(0, 0)) # devuelve el máximo común divisor de 0 y 0

print(math.isclose(0, 0)) # devuelve True si 0 y 0 son aproximadamente iguales

print(math.isclose(0, 0, rel_tol=0.1)) # devuelve True si 0 y 0 son aproximadamente iguales

print(math.isclose(0, 0, abs_tol=0.1)) # devuelve True si 0 y 0 son aproximadamente iguales
