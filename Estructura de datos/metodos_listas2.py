
lista = [1,2,3,45,6,76]

#count cuenta cuantas veces se repite un elemento en la lista

print(lista.count(2)) # cuenta cuantas veces aparece el elemento 1 en la lista

# index() devuelve la posicion de un elemento en la lista

print(lista.index(45)) # devuelve la posicion del elemento 45 en la lista

#sort() ordena la lista de menor a mayor
lista.sort() # este metodo se tiene que usar por aparte, solo ordena la lista de menor a mayor
print(lista) 

#reverse() invierte la lista
lista.reverse() # este metodo se tiene que usar por aparte, solo invierte la lista
print(lista)