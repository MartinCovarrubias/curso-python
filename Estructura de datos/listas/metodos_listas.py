
lista = [1, 2, 3, 4, 5, 6, "Hola", "Mundo", "Python"]

# append() agrega un elemento al final de la lista
# append(ELEMENTO)
lista.append("Nuevo elemento") # agrega el elemento "Nuevo elemento" al final de la lista
print(lista)

# insert() agrega un elemento en la posicion indicada
# insert(posicion, elemento)
lista.insert(0, "Nuevo elemento2") # agrega el elemento "Nuevo elemento" en la posicion 0
print(lista)