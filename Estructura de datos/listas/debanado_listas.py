# debanado de listas

lista = [1, 2, 3, 4, 5, 6, "Hola", "Mundo", "Python"]

print(lista[0:5]) # imprime los elementos de la lista desde el indice 0 hasta el indice 5

print(lista[:2]) # imprime los elementos de la lista desde el indice 0 hasta el indice 2

print(lista[2:]) # imprime los elementos de la lista desde el indice 2 hasta el final

print(lista[-5:-1]) # imprime los elementos de la lista desde el indice -5 hasta el indice -1

print(lista[::2]) # imprime los elementos de la lista desde el indice 0 hasta el final de 2 en 2