
cadena = "Esto es una cadena de texto" #strings van en comillas dobles o simples
print(cadena)

#escapar caracteres o string literals con \ *Ejemplo

cadena2 = "Esto es un ejemplo de \"string literal\""
print(cadena2)

cadena3 = 'Esto es un ejemplo de \'string literal\''
print(cadena3)

#salto de linea
cadena4 = "Esto es \nun ejemplo de\nsalto de linea"
print(cadena4)

#tabulacion
cadena5 = "Esto es \tun ejemplo de\ttabulacion"
print(cadena5)

#elimina espacio \b
cadena6 = "Esto es \bun ejemplo de\bborrado"
print(cadena6)

#retroceso \r
cadena7 = "Esto es \run ejemplo de\rborrado"
print(cadena7)

# \f form feed
cadena8 = "Esto es \fun ejemplo de\fborrado"
print(cadena8)

#\v simbolo vertical
cadena9 = "Esto es \vun ejemplo de borrado"
print(cadena9)

